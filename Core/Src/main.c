/* USER CODE BEGIN 4 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static uint16_t last_GPIO_Pin = 0;
	
	// This "if" prevent multiple button detections
	if(last_GPIO_Pin == GPIO_Pin && __HAL_TIM_GetCounter(&htim1) != 0)
		return;

	last_GPIO_Pin = GPIO_Pin;
	HAL_TIM_Base_Start(&htim1);
	
	if(GPIO_Pin == BTN_YELLOW_Pin)
			yellow_cpt++;
	else if(GPIO_Pin == BTN_BLUE_Pin)
		blue_cpt ++;
	
	switch(myAlarm.alarm_state){
		case OFF:
			if(GPIO_Pin == BTN_GREEN_Pin){
				change_state(&myAlarm, STARTING);
			}
			break;
			
		case STARTING:
			if(GPIO_Pin == BTN_RED_Pin && blue_cpt < 240){
					change_state(&myAlarm, OFF);
			}
			break;
			
		case ENGAGED:
			if(GPIO_Pin == BTN_RED_Pin){
					change_state(&myAlarm, OFF);
			}
			else if(GPIO_Pin == BTN_USER_Pin){
				change_state(&myAlarm, TRIGERRED);
			}
			break;
		
		case TRIGERRED:
			switch(GPIO_Pin){
				case BTN_YELLOW_Pin: case BTN_BLUE_Pin:
					for(char i = 0; i < 4; i++){
						if(myAlarm.user_current_pin[i] == 0){
							myAlarm.user_current_pin[i] = GPIO_Pin;
							if(i == 3){
								if(check_pin(&myAlarm)){
									playRightPin(&htim2);
									change_state(&myAlarm, OFF);
									HAL_Delay(200);
								}
								else{
									playWrongPin(&htim2);
									myAlarm.missed_pin_cpt ++;
									if(myAlarm.missed_pin_cpt >= 3)
										change_state(&myAlarm, ALARM);
								}
								reset_user_pin(&myAlarm);
							}
							break;
						}
					}
					break;
			}
			break;
				case ALARM:
					if(GPIO_Pin == BTN_BLACK_Pin)
						change_state(&myAlarm, ENGAGED);
					break;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim == &htim3){
		switch(myAlarm.alarm_state){
			case STARTING:
				HAL_GPIO_TogglePin(GPIOD, LED_ORANGE_Pin);
				myAlarm.starting_cpt ++;
				
				if(myAlarm.starting_cpt >= STARTING_TIME){
					change_state(&myAlarm, ENGAGED);
				}
			break;
			
			case TRIGERRED:
				HAL_GPIO_TogglePin(GPIOD, LED_RED_Pin);
				myAlarm.triggered_cpt ++;
			
				if(myAlarm.triggered_cpt >= MAX_PIN_TIME){
					change_state(&myAlarm, ALARM);
				}
			break;
			
			default:
				do_nothing();
		}
	}
}
